#casting
x = int(1)   #x=1
y = int(2.8) #y=2
z = int("3") #z=3
print(x)
print(y)
print(z)

x = float(1)     # x=1.0
y = float(2.8)   # y=2.8
z = float("3")   # z=3.0
w = float("4.2") # w=4.2
print(x)
print(y)
print(z)
print(w)

x = str("s1") # x=s1
y = str(2)    # y=2
z = str(3.0)  # z=3.0
print(x)
print(y)
print(z)






